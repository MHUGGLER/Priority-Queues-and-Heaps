import java.util.ArrayList;

class HeapPriorityQueue<K extends Comparable<K>> {
    private ArrayList<K> heap = new ArrayList<>();

    // Helper methods
    protected int parent(int j) { return (j - 1) / 2; }
    protected int left(int j) { return 2 * j + 1; }
    protected int right(int j) { return 2 * j + 2; }

    public int size() { return heap.size(); }
    public boolean isEmpty() { return heap.isEmpty(); }

    private void swap(int i, int j) {
        K temp = heap.get(i);
        heap.set(i, heap.get(j));
        heap.set(j, temp);
    }

    public void insert(K key) {
        heap.add(key);
        upheap(heap.size() - 1);
    }

    public K removeMin() {
        if (isEmpty()) return null;

        K answer = heap.get(0);
        K last = heap.remove(heap.size() - 1);

        if (!heap.isEmpty()) {
            heap.set(0, last);
            downheap(0);
        }

        return answer;
    }

    public K min() {
        return isEmpty() ? null : heap.get(0);
    }

    // -------------------------
    //        UPHEAP
    // -------------------------
    private void upheap(int j) {
        while (j > 0) {
            int p = parent(j);
            if (heap.get(j).compareTo(heap.get(p)) >= 0) break; // child >= parent → STOP
            swap(j, p);
            j = p; // continue moving up
        }
    }

    // -------------------------
    //       DOWNHEAP
    // -------------------------
    private void downheap(int j) {
        while (left(j) < heap.size()) { // while node has a left child
            int leftIdx = left(j);
            int smallChildIdx = leftIdx;

            int rightIdx = right(j);
            if (rightIdx < heap.size() &&
                heap.get(rightIdx).compareTo(heap.get(leftIdx)) < 0) {
                smallChildIdx = rightIdx;
            }

            // If parent <= smallest child → STOP
            if (heap.get(j).compareTo(heap.get(smallChildIdx)) <= 0) break;

            swap(j, smallChildIdx);
            j = smallChildIdx; // continue downward
        }
    }
}
